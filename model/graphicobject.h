#pragma once

/**
 * @brief Абстрактный базовый класс графического примитива.
 *
 * Представляет любой векторный объект, который может быть размещён
 * в документе редактора. Используется как базовый класс для всех
 * конкретных графических примитивов.
 *
 * Является частью Model в архитектуре MVC.
 */
class GraphicObject {
public:
    virtual ~GraphicObject() = default;

    /**
 * @brief Отрисовка графического примитива.
 *
 * Конкретная реализация зависит от типа примитива.
 */
    virtual void draw() const = 0;
};